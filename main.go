package main

import "fmt"

func main(){
	// declare variables
	var str string
	var n, m int
	var mn float32

	// assign variable
	str = "Hello world!"
	n, m = 10, 50
	mn = 2.45

	/* Print the result */
	fmt.Printf("value of str: %v\n", str)
	fmt.Printf("value of m: %v, n: %v\n", m, n)
	fmt.Printf("value of mn: %v\n", mn)
}